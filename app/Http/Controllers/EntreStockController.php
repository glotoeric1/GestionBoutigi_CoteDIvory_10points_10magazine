     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $qteEntre = 0;
        $qteSortie = 0;
        $obj = new EntreSortieStock();
        $datas = $obj->getAllLatest();
        foreach ($datas as $item) {
            if ($item->operation == "Entrer") {
                $qteEntre += $item->qte_en_stock;
            } else {
                $qteSortie += (int) $item->qte;
            }
        }
        return view("gestion_stock.index", compact("datas", "qteEntre", "qteSortie"));
    }

